// Step 1: Install TypeScript globally using npm
// Command: npm install -g typescript

// Step 2: Verify the installation
// Command: tsc --version

// Step 3: Create a new TypeScript file
// File: hello-world.ts

// Step 4: Write a simple TypeScript code
